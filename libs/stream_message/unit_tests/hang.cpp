// This program should hang. It is considered an error to lock the
// same Mutex twice from the same thread, and preferably we can
// implement threads consistently enough that we aren't afraid that
// something tested on one platform is going to fail on the next
// due to inconsistent handling of error cases.

#include "threads.h"

using namespace SAThreads;

int main (int argc, char *argv[]) {
    Mutex m;
    {
        Exclusion e1(m);
        {
	    Exclusion e2(m);
	}
    }
    return 0;
}
