int main ( int argc, char **argv )
{
  int retval = -1;

  if ( argc < 2 || argc > 3)
    {
      cerr << "usage: " << argv[0] << " IF-filename [-build-map]\n";
      return -1;
    }

  if ( argc == 3 )
    build_map = 1;

  retval = iff_parse(argv[1]);

  return retval;
}
