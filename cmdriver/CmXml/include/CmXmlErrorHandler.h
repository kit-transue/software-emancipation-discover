#ifndef CmXmlErrorHandler_HEADER_GUARD_
#define CmXmlErrorHandler_HEADER_GUARD_

#include <util/XercesDefs.hpp>
#include <sax/ErrorHandler.hpp>


class CmXmlErrorHandler : public ErrorHandler {

public:
    // -----------------------------------------------------------------------
    //  Constructor and Destructor
    // -----------------------------------------------------------------------
    CmXmlErrorHandler() {}
    virtual ~CmXmlErrorHandler() {} 

    // -----------------------------------------------------------------------
    //  Implementation of the error handler interface
    // -----------------------------------------------------------------------
    void warning(const SAXParseException& toCatch);
    void error(const SAXParseException& toCatch);
    void fatalError(const SAXParseException& toCatch);
    void resetErrors();

private:
    void printMessage(const char* prefix, const SAXParseException& toCatch);

}; //CmXmlErrorHandler


#endif //CmXmlErrorHandler_HEADER_GUARD_
