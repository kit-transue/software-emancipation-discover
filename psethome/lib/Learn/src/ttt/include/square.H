#ifndef _square_h_
#define _square_h_

// standard
#include <sys/stat.h>

// usage
#include "color.H"

class Square {

public:

  Square(): col(0), filled(0) { }
  virtual ~Square();

  virtual void reset();

  virtual void fill(color& c);
  virtual void clear();

  virtual void deactivate();
  virtual void activate();

  virtual void deemphasize();
  virtual void emphasize();

  virtual void save(char* filename);
  virtual void restore(char* filename);

  virtual int isFilled();
  virtual color& getColor();

protected:

  int filled;
  color* col;
};

#endif
