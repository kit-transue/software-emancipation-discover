/* access_invalidates_overload */
/* Seen in Sparcworks compilers. */

/* In standard C++, the fact that a conversion is illegal because it would
violate access restrictions is not considered in creating the list of viable
functions in doing overload resolution.  (It only matters if the function
actually selected would require an illegal conversion.) */


struct B { };
struct D: private B { };
void f(B&, int);
void f(D&, long);
void f() {
    D d;
    f(d, 0);
}
