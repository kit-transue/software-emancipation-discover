#include "general.h"

#define deftok(x,n) {quote(x),n}
#define tok_enum tok_name tok_name_arr[] = 

struct tok_name { char * name; int val;};
#include "smtScdTypes.h"

#undef deftok
#undef tok_enum

char * smt_token_name(int n) {
  int arr_len = sizeof(tok_name_arr) / sizeof(tok_name);
  for(int ii = 0; ii<arr_len; ++ii)
       if(tok_name_arr[ii].val == n)
              return(tok_name_arr[ii].name);
  return "undefined token";
}
