#ifndef _indHeaderInfo_h
#define _indHeaderInfo_h

// pmod.ind files begin with this pattern of bytes
//  note that this isn't portable to little-endian machines, since
//  they want to share the exact files.
class indHeaderInfo
{
public:
    long magic;			// Magic number  #IND
    long version;		// Version of save file format
    int date;			// Date of original header creation
    long checksum;		// Checksum of source file
    uint offset_to_data;	// offset from bof to begin of array of hashes
    uint dummy[3];		// reserve.  32 byte aligned for ease of od /x
};

#endif
